# Operadores aritméticos

a = 5
b = 3
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)  # Divisão inteira
print(a % b)  # Resto da divisão
print(a ** b)  # Potência

# Operadores por atribuição

c = 5  # 5
c = c + 2  # 7
c += 2  # 9
c *= 2  # 18
c /= 2  # 9
c -= 2  # 7
print(c)

# Precedência
d = 10 + (3 * 2) ** 2
print(d)

